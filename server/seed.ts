export async function seedData() {
  const existingInventory = await db.select().from(inventoryItems);
  if (existingInventory.length > 0) {
    console.log("Seed: data already exists, skipping");
    return;
  }

  console.log("Seed: inserting inventory data...");
  await db.insert(inventoryItems).values(INVENTORY_DATA);
  console.log(`Seed: inserted ${INVENTORY_DATA.length} inventory items`);

  console.log("Seed: inserting order data...");
  const batchSize = 50;
  for (let i = 0; i < ORDER_SEED_DATA.length; i += batchSize) {
    const batch = ORDER_SEED_DATA.slice(i, i + batchSize);
    await db.insert(orderRecords).values(batch);
  }
  console.log(`Seed: inserted ${ORDER_SEED_DATA.length} order records`);
}
